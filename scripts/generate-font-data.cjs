#!/usr/bin/env node
/*
 * Generate shared/fonts/font-data.ts from the vendored woff2 (assets/fonts/).
 *
 * The generated-document render iframe is sandbox="" (an opaque origin), so the
 * designed fonts can't be loaded by URL/asset:// — they ride as base64 data: URIs in
 * an injected @font-face (ADR-0013). This script base64-encodes the vendored,
 * latin-subset OFL fonts into a committed TS module that BOTH the renderer (Stage C
 * render wrapper) and main (Stage D export) import — one source of truth, no
 * bundler-specific asset-inlining (Vite ?inline is not honored uniformly under
 * vitest, so the data is materialized at this build step instead).
 *
 * Regenerate after changing the vendored fonts: npm run fonts:generate
 */
const fs = require('node:fs');
const path = require('node:path');

const root = path.resolve(__dirname, '..');
const dir = path.join(root, 'assets', 'fonts');

// key -> woff2 file. Keys match `${family}-${weight}` in shared/fonts/font-faces.ts.
const FILES = {
  'Merriweather-400': 'merriweather-400.woff2',
  'Merriweather-700': 'merriweather-700.woff2',
  'Inter-400': 'inter-400.woff2',
  'Inter-600': 'inter-600.woff2',
  'Inter-700': 'inter-700.woff2',
};

const entries = Object.entries(FILES).map(([key, file]) => {
  const b64 = fs.readFileSync(path.join(dir, file)).toString('base64');
  return `  '${key}': 'data:font/woff2;base64,${b64}',`;
});

const out = `// GENERATED by scripts/generate-font-data.cjs — DO NOT EDIT BY HAND.
// Base64 data: URIs for the vendored, latin-subset OFL fonts (assets/fonts/; see
// assets/fonts/NOTICE.md + docs/adr/0013-self-host-generated-doc-fonts.md).
// Regenerate with: npm run fonts:generate
/* eslint-disable */
export const FONT_DATA_URIS = {
${entries.join('\n')}
} as const;
`;

const target = path.join(root, 'shared', 'fonts', 'font-data.ts');
fs.writeFileSync(target, out, 'utf8');
console.log(`wrote ${path.relative(root, target)} (${out.length} bytes, ${entries.length} faces)`);
